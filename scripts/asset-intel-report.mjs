// LeavenWealth Asset Management — weekly Owner's Brief. Classifies the portfolio,
// builds per-meeting agendas, writes Collin's owner brief with a grounded AI
// executive summary, renders a PDF to Drive, emails it to Collin, records it in
// am_reports, and back-fills the AI summary onto the latest am_snapshot so the
// dashboard shows it too.
//
//   BigQuery → PGO_BQ_* | Google → GMAIL_* + GDRIVE_AM_FOLDER_ID | AI → ANTHROPIC_API_KEY
//   Email    → AM_REPORT_TO (default collinschwartz1@gmail.com)
//   Supabase → NEXT_PUBLIC_SUPABASE_URL / SUPABASE_SERVICE_ROLE_KEY
//
// Run:  node scripts/asset-intel-report.mjs   (cloud, weekly before meetings)

import { readFileSync, existsSync } from "node:fs";
import { join } from "node:path";
import { createClient } from "@supabase/supabase-js";
import { gather, gatherAnalysisData, bqReady } from "./pgo-bq.mjs";
import { analyze } from "./pgo-analyze.mjs";
import { classify, diffClassification, buildAgendas, ownerBrief, aiOwnerSummary } from "./asset-intel.mjs";
import { googleToken, htmlToDrivePdf, sendEmail, weekId } from "./lib-google.mjs";

function loadEnv() {
  const p = join(process.cwd(), ".env.local");
  if (!existsSync(p)) return;
  for (const line of readFileSync(p, "utf8").split("\n")) {
    const m = line.match(/^([A-Z0-9_]+)=(.*)$/);
    if (m && !process.env[m[1]]) process.env[m[1]] = m[2].trim();
  }
}
loadEnv();

const SUPA_URL = process.env.NEXT_PUBLIC_SUPABASE_URL;
const SUPA_KEY = process.env.SUPABASE_SERVICE_ROLE_KEY;
const FOLDER = process.env.GDRIVE_AM_FOLDER_ID || process.env.GDRIVE_PGO_FOLDER_ID;
const MAIL_TO = process.env.AM_REPORT_TO || process.env.PGO_REPORT_TO || "collinschwartz1@gmail.com";
if (!SUPA_URL || !SUPA_KEY) { console.error("am-report: missing Supabase creds."); process.exit(1); }
if (!bqReady()) { console.error("am-report: missing PGO BigQuery creds."); process.exit(1); }

const db = createClient(SUPA_URL, SUPA_KEY, { auth: { persistSession: false } });
const usd = (n) => (n == null ? "—" : `$${Math.round(Number(n)).toLocaleString("en-US")}`);

function buildHtml(brief, ai, cls, agendas, week) {
  const list = (items) => items.length ? `<ul>${items.map((s) => `<li>${s}</li>`).join("")}</ul>` : `<p style="color:#999">None.</p>`;
  const aiBlock = ai
    ? `<div style="background:#f5f8ff;border:1px solid #d6e2ff;border-radius:6px;padding:14px 16px;margin:10px 0">
         <div style="font-weight:700;font-size:14px;margin-bottom:6px">${ai.headline}</div>
         <ul>${ai.bullets.map((b) => `<li style="margin:4px 0">${b}</li>`).join("")}</ul></div>`
    : "";
  const tier = (rows) => rows.length
    ? `<ul>${rows.map((p) => `<li><b>${p.name}</b> — ${usd(p.noi)} NOI · ${p.reasons.join("; ")}</li>`).join("")}</ul>`
    : `<p style="color:#999">None.</p>`;
  const agendaBlock = Object.values(agendas).map((a) => {
    const secs = Object.entries(a.sections).filter(([, it]) => it && it.length);
    if (!secs.length) return "";
    return `<h3 style="margin:14px 0 4px;font-size:13px">${a.title}</h3>` +
      secs.map(([n, it]) => `<div style="color:#555;font-size:11px;margin-top:6px"><b>${n}</b></div><ul>${it.map((s) => `<li>${s}</li>`).join("")}</ul>`).join("");
  }).join("");

  return `<!doctype html><html><head><meta charset="utf-8"><style>
    body{font-family:Arial,Helvetica,sans-serif;color:#111;margin:40px;font-size:13px}
    h1{font-size:22px;margin:0 0 2px} h2{font-size:14px;margin:20px 0 6px;color:#1f6feb;border-bottom:2px solid #eee;padding-bottom:4px}
    ul{margin:4px 0;padding-left:18px} li{margin:2px 0} .sub{color:#777;margin-bottom:8px}
  </style></head><body>
    <h1>LeavenWealth — Owner's Weekly Asset Brief</h1>
    <div class="sub">${week} · financials ${brief.period} · ${brief.headlineStats.red} Red Zone · ${brief.headlineStats.nonStab} Non-Stabilized · generated by Jarvis</div>

    <h2>What to Raise This Week</h2>
    ${aiBlock}

    <h2>Escalate / Reclassify</h2>${list(brief.propertiesToEscalate)}
    <h2>Expenses to Question</h2>${list(brief.expensesToQuestion)}
    <h2>Collections to Press</h2>${list(brief.collectionsToPress)}
    <h2>Trends to Watch</h2>${list(brief.trendsToWatch)}
    ${brief.statusChanges.length ? `<h2>Status Changes</h2>${list(brief.statusChanges)}` : ""}
    ${brief.orphans.length ? `<h2>No Meeting Home (needs a place)</h2>${list(brief.orphans)}` : ""}

    <h2>Red Zone</h2>${tier(cls.redZone)}
    <h2>Non-Stabilized</h2>${tier(cls.nonStabilized)}

    <h2>Meeting Agendas</h2>${agendaBlock}

    <div style="margin-top:24px;color:#999;font-size:10px">
      Confidential — owner brief. Live from Buildium (BigQuery) + the LeavenWealth Meeting Operating System.
      Covers ${cls.counts.total} PGO-managed assets; LW assets outside the PGO data feed are not yet included.
    </div>
  </body></html>`;
}

async function main() {
  const week = weekId();
  const f = await gather();
  const analysis = analyze(f, await gatherAnalysisData());

  const { data: prev } = await db.from("am_snapshot").select("id,raw").order("captured_at", { ascending: false }).limit(1).maybeSingle();
  const prevAll = prev?.raw?.classification?.all || null;

  const cls = classify(f, analysis);
  const changes = diffClassification(cls, prevAll);
  const agendas = buildAgendas(cls, analysis, f, changes);
  const brief = ownerBrief(cls, analysis, f, changes);
  const ai = await aiOwnerSummary(brief, cls);
  console.log(`am-report: Red ${cls.counts.red} · Non-Stab ${cls.counts.nonStab} · Stab ${cls.counts.stab}${ai ? " · AI ok" : " · AI skipped"}.`);

  const html = buildHtml(brief, ai, cls, agendas, week);
  const name = `LW Owner Brief — ${week}`;

  let drive = { id: null, webViewLink: null };
  let token = null;
  try {
    token = await googleToken();
    drive = await htmlToDrivePdf(token, html, name, FOLDER);
    console.log(`am-report: PDF → ${drive.webViewLink}`);
  } catch (e) { console.error("am-report: Drive step failed:", e.message); }

  try {
    if (!token) token = await googleToken();
    const subj = ai && ai.headline ? `LW Owner Brief (${week}) — ${ai.headline}`.slice(0, 140) : `LW Owner Brief — ${week} (${cls.counts.red} Red Zone)`;
    await sendEmail(token, MAIL_TO, subj, html);
    console.log(`am-report: emailed → ${MAIL_TO}`);
  } catch (e) { console.error("am-report: email failed:", e.message); }

  // back-fill the AI summary onto the latest snapshot so the dashboard shows it
  if (prev?.id && ai) {
    await db.from("am_snapshot").update({ raw: { ...prev.raw, ownerBrief: brief, ai } }).eq("id", prev.id)
      .then(({ error }) => error && console.error("am-report: snapshot ai update error", error.message));
  }

  const { error } = await db.from("am_reports").upsert(
    { period: week, drive_file_id: drive.id, web_view_link: drive.webViewLink, title: name, generated_at: new Date().toISOString() },
    { onConflict: "period" }
  );
  if (error) console.error("am-report: upsert error", error.message);
  else console.log(`am-report: recorded ${week}.`);
}

main().catch((e) => { console.error("am-report: fatal", e); process.exit(1); });
